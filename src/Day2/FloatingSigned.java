package Day2;

public class FloatingSigned {

    public static void main(String[] args)
    {
        //float
        System.out.println("\nfloat operation ");
        float w1 = 0.123456789f;
        float q = (float)12.123456789;
        float r = 1.123456789F;
        System.out.println(w1);
        System.out.println(q );
        System.out.println(r);

        //double
        System.out.println("\ndouble operation ");
        double w2 = 2.123456789123456489 ;
        System.out.println(w2);


    }
}
